//floyd triangle


import java.util.*;
class p8
{
    public static void main(String args[])
    {
        int y=0;

        for ( int i=0;i<5;i++)
        {
            for ( int j =0;j<=i;j++)
            {
                  y++;
                  System.out.print(" "+y);
            }
            System.out.println();
        }
    }
}